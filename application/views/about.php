    <h2>Mission</h2>
    <p>Welcome to the Obstacle Course Race Training 4 ME website.  This site was built to be a tool for obstacle course racers and those interested in getting into the sport. Obstacle course race training combines the cardiovascular demands of running with functional fitness activities such as climbing, crawling, and carrying heavy object.</p>
    <p>A fun way to train for these races is to combine runs with strength sessions at various public venues that can targe the muscles needed to complete common obstacles.  Examples of such venues include public parks, wooded trails, and Vita Course stations.</p>
    <p>This site offers three sections to help users train for and compete in an obstacle course race</p>
    <h3>Explore</h3>
    <p>In this section, users can explore a map and see user submitted locations where athletes and perform exercises that will prepare them for an obstacle course race.  Registered users can submit new locations for review.  They may also rate existing locations.</p>
    <h3>Train</h3>
    <p>In this section, users can explore users submitted workouts that use one or more locations.  Registered users can create new workouts and rate existing workouts.
    <h3>Race</h3>
    <p>In this section users can explore a list of upcoming races.
    <h2>My Bio</h2>
    <p>I have been an athlete of some sort my entire life.  I began with team sports such as baseball and basketball.  In high-school I started gravitating toward more individual sports like tennis and swimming.  After college, I started cycling which led to me doing a few triathlons despite not liking running. After a few years of doing nothing, I started running again after reading an article about natural running.  The form change made the activity much more enjoyable.  I've done several half marathons and target about 1 race a year.  I did another couple of triathlons despite not doing a lot of swimming and biking.</p>
    <p>This site is not meant to advertise for any one race series, but I have to acknowledge the role the Spartan series has played.  I first learned about them at an expo for one of my half-marathons and was immediately intrigued. I found a different series with a local event and did by first race in 2013.  What I discovered was that not only did I like doing the race, but I enjoyed training for the race.  I liked triathlons for the training variety they demanded and obstacle course race training took that variety to the next level.  My training runs started to include stops for body weight exercises.  I found myself looking for things to climb, jump over, or carry.  Playgrounds immediately became frequent stops.  When traveling to different cities, I would explore the area and note places that would be a good candidate for training.  Then, I would run from place to place and make up routines based on the equipment available.  Back home, it didn't take long to find lots of great locations, so many that I began having trouble remembering where I had been and what was there.  Thus, the idea for this site was born.</p>
    <p>I'm a software engineer by trade.  I don't develop as much anymore, so this site also gives me the opportunity to try and stay informed regarding the latest technology.  One thing that should be very obvious is that I am not a UI person.  I have lots of ideas for functionality that I will implement as time allows.  However, unless some good Samaritan or two on GitHub lends a hand, this site will never be snazzy or even pleasant to see.</p>
